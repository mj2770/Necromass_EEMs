def regional_mean_manual(dataset, ex_min, ex_max, em_min, em_max):
    X = dataset.eem_stack
    ex = np.asarray(dataset.ex_range)
    em = np.asarray(dataset.em_range)
    ex_mask = (ex >= ex_min) & (ex <= ex_max)
    em_mask = (em >= em_min) & (em <= em_max)
    block = X[:, ex_mask, :][:, :, em_mask]
    return pd.Series(block.mean(axis=(1,2)), index=dataset.index, name="regional_mean")

