# Regional integration
# sum fluorescence over a rectangle: Ex [250,300], Em [380,450]
# dataset_proc.eem_stack is (n_samples, n_ex, n_em)
def regional_integration_manual(dataset, ex_min, ex_max, em_min, em_max):
    X = dataset.eem_stack
    ex = np.asarray(dataset.ex_range)
    em = np.asarray(dataset.em_range)

    ex_mask = (ex >= ex_min) & (ex <= ex_max)
    em_mask = (em >= em_min) & (em <= em_max)

    vals = X[:, ex_mask, :][:, :, em_mask].sum(axis=(1, 2))
    return pd.Series(vals, index=dataset.index, name="regional_integration")

