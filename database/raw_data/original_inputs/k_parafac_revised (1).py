def find_2d_mats(obj):
    mats = []
    for k, v in getattr(obj, "__dict__", {}).items():
        try:
            arr = np.asarray(v)
        except Exception:
            continue
        if arr.ndim == 2:
            mats.append((k, arr))
    return mats

def extract_scores_and_loadings(parafac, dataset):
    """
    Returns:
      scores: (n_samples, R)
      ex_load: (n_ex, R)
      em_load: (n_em, R)
    Works across eempy versions by matching shapes.
    """
    n_samples = dataset.eem_stack.shape[0]
    n_ex = len(dataset.ex_range)
    n_em = len(dataset.em_range)

    # gather candidate matrices from parafac and nested objects
    cand = find_2d_mats(parafac)
    for k, v in getattr(parafac, "__dict__", {}).items():
        cand += [(f"{k}.{kk}", arr) for kk, arr in find_2d_mats(v)]

    # find scores/loadings by shape
    ex_load = next((arr for _, arr in cand if arr.shape[0] == n_ex and arr.shape[1] >= 2), None)
    em_load = next((arr for _, arr in cand if arr.shape[0] == n_em and arr.shape[1] >= 2), None)
    scores  = next((arr for _, arr in cand if arr.shape[0] == n_samples and arr.shape[1] >= 2), None)

    # sometimes loadings are transposed (R, n_ex); handle that
    if ex_load is None:
        ex_load = next((arr.T for _, arr in cand if arr.shape[1] == n_ex and arr.shape[0] >= 2), None)
    if em_load is None:
        em_load = next((arr.T for _, arr in cand if arr.shape[1] == n_em and arr.shape[0] >= 2), None)
    if scores is None:
        scores = next((arr.T for _, arr in cand if arr.shape[1] == n_samples and arr.shape[0] >= 2), None)

    if ex_load is None or em_load is None or scores is None:
        # print helpful info
        print("Could not uniquely detect scores/loadings. Candidate shapes:")
        for name, arr in cand[:40]:
            print(name, arr.shape)
        raise AttributeError("Could not extract PARAFAC scores/loadings from this eempy PARAFAC object.")

    # R is the common number of components
    R = min(ex_load.shape[1], em_load.shape[1], scores.shape[1])
    return scores[:, :R], ex_load[:, :R], em_load[:, :R]

def compute_fmax_table(parafac, dataset):
    scores, ex_load, em_load = extract_scores_and_loadings(parafac, dataset)
    ex_max = np.max(ex_load, axis=0)   # (R,)
    em_max = np.max(em_load, axis=0)   # (R,)
    fmax = scores * (ex_max * em_max)  # (n_samples, R)

    cols = [f"C{i+1}_Fmax" for i in range(fmax.shape[1])]
    return pd.DataFrame(fmax, index=list(dataset.index), columns=cols)

