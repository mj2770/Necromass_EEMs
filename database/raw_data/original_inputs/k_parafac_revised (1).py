# Reader eem tables into 2D matrix
def read_eem_table(fp: Path):
    header1 = pd.read_csv(fp, nrows=1, header=None)
    ex = pd.to_numeric(header1.iloc[0, 1:], errors="coerce").values.astype(float)

    df = pd.read_csv(fp, skiprows=2, header=None)
    em = pd.to_numeric(df.iloc[:, 0], errors="coerce").values.astype(float)
    z  = df.iloc[:, 1:].apply(pd.to_numeric, errors="coerce").to_numpy(dtype=float)

    z = np.nan_to_num(z, nan=0.0)
    # Confirmed with Bri that all files are already blank-subtracted, negative values can appear.
    # Clamp to non-negative values for nonnegative PARAFAC.
    z[z < 0] = 0.0

    # z is (n_em, n_ex) -> return (n_ex, n_em) for stacking
    return z.T, ex, em

