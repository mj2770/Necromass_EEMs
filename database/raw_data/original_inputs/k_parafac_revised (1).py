import numpy as np
import matplotlib.pyplot as plt

def plot_eem_panel(
    eems,                  # dict: {title: eem_matrix (n_ex, n_em)}
    ex_range, em_range,    # 1D arrays
    figsize=(22, 5),
    title_fontsize=16,
    label_fontsize=13,
    tick_fontsize=11,
    cmap="jet",
    vmin=None, vmax=None,  # set to None for autoscale; or pass numbers for consistent scaling
):
    names = list(eems.keys())
    mats = [np.asarray(eems[k]) for k in names]

    # If you want consistent color scaling across panels:
    if vmin is None:
        vmin = min(np.nanmin(m) for m in mats)
    if vmax is None:
        vmax = max(np.nanmax(m) for m in mats)

    fig, axes = plt.subplots(
        1, len(names),
        figsize=figsize,
        constrained_layout=True,
        sharex=True, sharey=True
    )

    if len(names) == 1:
        axes = [axes]

    # pcolormesh coordinate edges (makes axes match wavelengths cleanly)
    # If your ex/em are evenly spaced, this works well:
    ex = np.asarray(ex_range)
    em = np.asarray(em_range)

    # Build edges for pcolormesh
    def edges(x):
        x = np.asarray(x)
        dx = np.diff(x)
        # assume roughly regular spacing; extend ends
        left = x[0] - dx[0]/2
        right = x[-1] + dx[-1]/2
        mid = x[:-1] + dx/2
        return np.concatenate([[left], mid, [right]])

    ex_edges = edges(ex)
    em_edges = edges(em)

    im = None
    for ax, name, Z in zip(axes, names, mats):
        # Z is (n_ex, n_em), so x-axis = emission, y-axis = excitation
        im = ax.pcolormesh(em_edges, ex_edges, Z, shading="auto", cmap=cmap, vmin=vmin, vmax=vmax)

        ax.set_title(name, fontsize=title_fontsize, pad=8)
        ax.tick_params(axis="both", labelsize=tick_fontsize)

    # Shared axis labels
    axes[0].set_ylabel("Excitation (nm)", fontsize=label_fontsize)
    for ax in axes:
        ax.set_xlabel("Emission (nm)", fontsize=label_fontsize)

    # One shared colorbar
    cbar = fig.colorbar(im, ax=axes, shrink=0.9, pad=0.02)
    cbar.set_label("Intensity (a.u.)", fontsize=label_fontsize)
    cbar.ax.tick_params(labelsize=tick_fontsize)

    plt.show()